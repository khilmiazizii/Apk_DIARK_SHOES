    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {  // Tidak ada baris yang dipilih
            JOptionPane.showMessageDialog(null, "Pilih barang terlebih dahulu.");
            return;
       }
    try {
        // Validasi: pastikan field tidak kosong
        if (jTextField2.getText().trim().isEmpty() ||
            jTextField1.getText().trim().isEmpty() ||
            jTextField3.getText().trim().isEmpty() ||
            jTextField4.getText().trim().isEmpty() ||
            jTextField5.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Semua field harus diisi!");
            return;
        }

        // Konversi Stok dan Harga ke Integer
        int stok = Integer.parseInt(jTextField3.getText().trim());
        int harga = Integer.parseInt(jTextField4.getText().trim());

        // SQL untuk update data
        String sql = "UPDATE data_sepatu SET Nama_sepatu=?, Stok=?, Tipe=?, Harga=? WHERE Kode_sepatu=?";
        pst = koneksi.prepareStatement(sql);
        pst.setString(1, jTextField1.getText().trim());
        pst.setInt(2, stok);
        pst.setString(3, jTextField5.getText().trim());
        pst.setInt(4, harga);
        pst.setString(5, jTextField2.getText().trim());

        pst.executeUpdate();

        // Refresh tabel dan tampilkan pesan sukses
        update_tabel();
        JOptionPane.showMessageDialog(null, "Data berhasil diupdate!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Stok dan Harga harus berupa angka!");
    } catch (HeadlessException | SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
    }
        clsr();

    }//GEN-LAST:event_jButton6ActionPerformed

