    private void simpan() {
    try {
    // Validasi input kosong
    if (jTextField1.getText().isEmpty() || 
        jTextField2.getText().isEmpty() || 
        jTextField3.getText().isEmpty() || 
        jTextField4.getText().isEmpty() || 
        jTextField5.getText().isEmpty()) {
        
        JOptionPane.showMessageDialog(null, "Semua kolom harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return; // hentikan proses
    }

    // Validasi angka untuk stok dan harga
    int stok = Integer.parseInt(jTextField3.getText());
    int harga = Integer.parseInt(jTextField4.getText());

    // Cek apakah kode barang sudah ada
    String checkSql = "SELECT COUNT(*) FROM data_sepatu WHERE Kode_sepatu = ?";
    pst = koneksi.prepareStatement(checkSql);
    pst.setString(1, jTextField2.getText());  // kode barang
    rst = pst.executeQuery();
    rst.next();
    int count = rst.getInt(1);

    if (count > 0) {
        JOptionPane.showMessageDialog(null, "Kode barang sudah ada. Silakan masukkan kode barang lain.");
    } else {
        // Simpan data ke database
        String sql = "INSERT INTO data_sepatu (Kode_sepatu, Nama_sepatu, Stok, Tipe, Harga) VALUES (?, ?, ?, ?, ?)";
        pst = koneksi.prepareStatement(sql);
        pst.setString(1, jTextField2.getText());  // Kode barang
        pst.setString(2, jTextField1.getText());  // Nama barang
        pst.setInt(3, stok);                      // Stok
        pst.setString(4, jTextField5.getText());  // Tipe
        pst.setInt(5, harga);                     // Harga
        pst.execute();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan.");
        update_tabel();  // refresh tabel
        clsr(); // kosongkan form input
    }
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(null, "Kolom Stok dan Harga harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
} catch (HeadlessException | SQLException e) {
    JOptionPane.showMessageDialog(null, e);
}

}



