    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
    // Ambil data ID dari baris yang dipilih di JTable
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus.");
        return;
    }
    
    String id = jTable1.getValueAt(selectedRow, 0).toString();  // Ambil ID dari tabel
    
    // Konfirmasi penghapusan
    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus data ini?", 
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        String deleteQuery = "DELETE FROM data_admin WHERE id = ?";
        
        try (Connection koneksi = DatabaseConnection.connect();
             PreparedStatement stmt = koneksi.prepareStatement(deleteQuery)) {

            stmt.setInt(1, Integer.parseInt(id));
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                tampilkanDataUser();  // Menampilkan data yang sudah dihapus
            } else {
                JOptionPane.showMessageDialog(this, "ID tidak ditemukan.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed

