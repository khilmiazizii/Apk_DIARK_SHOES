    private void btnTambahDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahDataActionPerformed
        // TODO add your handling code here:
                String username = jTextField1.getText().trim();
String rawPassword = new String(jPasswordField1.getPassword()).trim();
String password = HashUtil.hashPassword(rawPassword);

if (username.isEmpty() || password.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong.");
    return;
}
try {
    Connection koneksi = DatabaseConnection.connect();
    String cekQuery = "SELECT COUNT(*) FROM data_admin WHERE username = ?";
    PreparedStatement cekStmt = koneksi.prepareStatement(cekQuery);
    cekStmt.setString(1, username);
    ResultSet rs = cekStmt.executeQuery();
    rs.next();
    if (rs.getInt(1) > 0) {
        JOptionPane.showMessageDialog(this, "Username sudah digunakan!");
        return;
    }

    String insertQuery = "INSERT INTO data_admin (username, password) VALUES (?, ?)";
    PreparedStatement stmt = koneksi.prepareStatement(insertQuery);
    stmt.setString(1, username);
    stmt.setString(2, password);
    stmt.executeUpdate();
    JOptionPane.showMessageDialog(this, "Data berhasil disimpan.");
    jTextField1.setText("");
    jPasswordField1.setText("");
} catch (HeadlessException | SQLException e) {
    JOptionPane.showMessageDialog(this, "Gagal menyimpan: " + e.getMessage());
}
    }//GEN-LAST:event_btnTambahDataActionPerformed

