    private void tampilkanDataUser() {
    String sql = "SELECT id, username FROM data_admin ORDER BY id ASC";
    
    try (Connection koneksi = DatabaseConnection.connect();
         PreparedStatement stmt = koneksi.prepareStatement(sql);
         ResultSet rs = stmt.executeQuery()) {
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Username");

        // Mengambil data dari ResultSet dan memasukkannya ke dalam model tabel
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("username"),
            });
        }

        // Menampilkan model ke dalam JTable
        jTable1.setModel(model);
        
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}

